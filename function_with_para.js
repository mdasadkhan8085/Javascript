function myAddFunction(a, b, c) {
    console.log(a + b + c);
};

function mySubFunction(a, b, c) {
    console.log(a - b - c);
};

function myMultiplyFunction(a, b, c) {
    console.log(a * b * c);
};

function myDevideFunction(a, b, c) {
    console.log(a / b / c);
};

myAddFunction(5, 6, 10);
mySubFunction(50, 16, 10);
myMultiplyFunction(2, 4, 5);
myDevideFunction(10, 20, 20);