let age = '20';

//Euqal To
if(age == 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}

//Not Euqal To
if(age != 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}

//Greater Then
if(age < 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}

//Less Then
if(age > 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}

//Less Then Equal to
if(age >= 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}

//Greater Then Equal to
if(age <= 20){
    console.log("you can vote");
}else{
    console.log("you cannot vote");
}
